    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        String databaseurl = url.getText();
        String password1 = password.getText();
        String user1 = user.getText();

        try {
            DatabaseSettings set = new DatabaseSettings(databaseurl, user1, password1);

            // write object to file
            FileOutputStream fos = new FileOutputStream("settings.txt");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(set);
            oos.close();

            // read object from file
            FileInputStream fis = new FileInputStream("settings.txt");
            ObjectInputStream ois = new ObjectInputStream(fis);
            DatabaseSettings result = (DatabaseSettings) ois.readObject();
            ois.close();
            JOptionPane.showMessageDialog(null, "Database settings saved !");
            try {
                Connection conn = DriverManager.getConnection(result.getDatabaseURL(), result.getUsername(), result.getPassword());
                JOptionPane.showMessageDialog(null, "You are connected to the database");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "There was an error while \nconnecting to the database");
                System.err.println(e.getMessage());
            }
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "File not found");
        } catch (IOException e) {
            System.out.println("Error initializing stream");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DatabaseSetting.class.getName()).log(Level.SEVERE, null, ex);
        }
        // TODO Auto-generated catch block


    }//GEN-LAST:event_jMenuItem1ActionPerformed

