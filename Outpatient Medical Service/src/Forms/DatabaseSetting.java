    private void defaultSettings() {

        try {
            FileInputStream fis = new FileInputStream("settings.txt");
            ObjectInputStream ois = new ObjectInputStream(fis);
            DatabaseSettings result = (DatabaseSettings) ois.readObject();
            ois.close();

            url.setText(result.getDatabaseURL());
            user.setText(result.getUsername());
            password.setText(result.getPassword());

            try {
                Connection conn = DriverManager.getConnection(result.getDatabaseURL(), result.getUsername(), result.getPassword());
                JOptionPane.showMessageDialog(null, "You are connected to the database");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "There was an error while \nconnecting to the database");
                System.err.println(e.getMessage());
            }

        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "File not found");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error initializing stream");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DatabaseSetting.class.getName()).log(Level.SEVERE, null, ex);
        }
        // TODO Auto-generated catch block
    }

