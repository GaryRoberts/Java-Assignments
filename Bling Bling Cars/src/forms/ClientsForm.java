    private void loadClients() {
        // TODO add your handling code here:
        DefaultListModel list = new DefaultListModel();
        try {

            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/CarSystem", "gary", "1995");

            String query = "SELECT * FROM CLIENT";
            Statement st = conn.createStatement();

            ResultSet rs = st.executeQuery(query);

            int num = 0;
            while (rs.next()) {

                String carId = rs.getString("CLIENTID");
                String carName = rs.getString("FIRSTNAME");
                String chosen = "[" + carId + "]" + "-" + "[" + carName + "]";

                list.addElement(chosen);
                num++;
            }
            clientList.setModel(list);

            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An error occured");
            System.err.println(e.getMessage());
        }
    }

