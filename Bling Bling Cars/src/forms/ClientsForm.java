    private void clientListMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clientListMousePressed
        // TODO add your handling code here:
        String chosen = clientList.getSelectedValue();

        try {

            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/CarSystem", "gary", "1995");

            String query = "SELECT * FROM CLIENT";
            Statement st = conn.createStatement();

            ResultSet rs = st.executeQuery(query);
            int num = 0;
            while (rs.next()) {
                String clientId = rs.getString("CLIENTID");
                String firstName = rs.getString("FIRSTNAME");
                String lastName = rs.getString("LASTNAME");
                String gender = rs.getString("GENDER");
                String street = rs.getString("STREET");
                String district = rs.getString("DISTRICT");
                String parish = rs.getString("PARISH");
                String telephone = rs.getString("TELEPHONE");

                String chosen1 = "[" + clientId + "]" + "-" + "[" + firstName + "]";
                if (chosen1.equals(chosen)) {
                    clientIdField.setText(clientId);
                    firstNameField.setText(firstName);
                    lastNameField.setText(lastName);
                    genderComboBox.setSelectedItem(gender);
                    streetField.setText(street);
                    districtField.setText(district);
                    telephoneField.setText(telephone);
                    parishComboBox.setSelectedItem(parish);

                }
            }

            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An error occured");
            System.err.println(e.getMessage());
        }

    }//GEN-LAST:event_clientListMousePressed

